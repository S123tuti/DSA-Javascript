// ===============================================using WHILE LOOP =================================================================================
//  work on sorted array

let a1 = [2,3,45,65, 78, 98];
let a2 = [4,6,8,90]
let a3  = []
let m=0,  n=0, o=0;
while(m<a1.length && n<a2.length){
if(a1[m]<a2[n]){
    a3[o]=a1[m]
    m++
}else{
a3[o]=a2[n]
n++
}
o++
}
while(m<a1.length){
    a3[o]=a1[m]
    m++
    o++
}
console.log(a3);